package com.class01;

public class ConvertStringInteger {

	public static void main(String[] args) {
		
		String str="10";
		int add=Integer.parseInt(str)+24;
		System.out.println(add);
		
		double add2=Double.parseDouble(str)+2.0;
		System.out.println("Double "+add2);
		
		String a="5";
		
		int addition=Integer.parseInt(a)+10;
		System.out.println(addition);

	}

}
