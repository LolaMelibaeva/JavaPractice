package com.class01;

public class Multiplcation {

	public static void main(String[] args) {
		
		int max=10;
	
		 for (int i = 1; i <= max; i++) {
	            for (int j = 1; j <= max; j++) {
	                System.out.println(i+"*"+j+"="+i*j);
	            }
	            System.out.println();
	        }
	    }
	}

